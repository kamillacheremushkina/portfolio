function Cases() {
  const [currentIndex, setCurrentIndex] = useState(0)
  const [isExpanded, setIsExpanded] = useState(false)

  const currentCase = cases[currentIndex]

  useEffect(() => {
    setIsExpanded(false)
  }, [currentIndex])

  const goPrev = () => {
    setCurrentIndex((prev) => (prev === 0 ? cases.length - 1 : prev - 1))
  }

  const goNext = () => {
    setCurrentIndex((prev) => (prev === cases.length - 1 ? 0 : prev + 1))
  }

  const visibleBlocks = currentCase.details.slice(0, 2)
  const hiddenBlocks = currentCase.details.slice(2)

  return (
    <section className="cases-section">
      <div className="cases-slider">
        <div className="cases-header">
          <button type="button" className="cases-arrow cases-arrow--prev" onClick={goPrev} aria-label="Предыдущий кейс">
            <svg width="14" height="24" viewBox="0 0 14 24" fill="none" xmlns="http://www.w3.org/2000/svg">
              <path d="M13 1L2 12L13 23" stroke="currentColor" strokeWidth="1" strokeLinecap="round" strokeLinejoin="round"/>
            </svg>
          </button>

          <h1 className="cases-title">{currentCase.title}</h1>

          <button type="button" className="cases-arrow cases-arrow--next" onClick={goNext} aria-label="Следующий кейс">
            <svg width="14" height="24" viewBox="0 0 14 24" fill="none" xmlns="http://www.w3.org/2000/svg">
              <path d="M1 1L12 12L1 23" stroke="currentColor" strokeWidth="1" strokeLinecap="round" strokeLinejoin="round"/>
            </svg>
          </button>
        </div>

        <div className="cases-meta-info">
          {currentCase.role && <p>{currentCase.role}</p>}
          {currentCase.format.split('\n').map((line, i) => (
            <p key={i}>{line}</p>
          ))}
          {currentCase.period && <p>{currentCase.period}</p>}
        </div>

        <div className="cases-text-area">
          {visibleBlocks.map((block, index) => (
            <div className="cases-block" key={`visible-${index}`}>
              <h2>{block.heading}</h2>
              <p>
                {block.text.split('\n').map((line, i) => (
                  <span key={i}>
                    {line}
                    <br />
                  </span>
                ))}
              </p>
            </div>
          ))}

          {isExpanded && (
            <div className="cases-block-expanded">
              {hiddenBlocks.map((block, index) => (
                <div className="cases-block" key={`hidden-${index}`}>
                  <h2>{block.heading}</h2>
                  <p>
                    {block.text.split('\n').map((line, i) => (
                      <span key={i}>
                        {line}
                        <br />
                      </span>
                    ))}
                  </p>
                </div>
              ))}
            </div>
          )}

          <button
            type="button"
            className="cases-toggle-btn"
            onClick={() => setIsExpanded((prev) => !prev)}
          >
            {isExpanded ? 'Свернуть' : 'Читать подробнее'}
            <svg
              className={`cases-toggle-icon ${isExpanded ? 'is-open' : ''}`}
              width="12"
              height="8"
              viewBox="0 0 12 8"
              fill="none"
              xmlns="http://www.w3.org/2000/svg"
            >
              <path d="M1 1.5L6 6.5L11 1.5" stroke="currentColor" strokeWidth="1" strokeLinecap="round" strokeLinejoin="round"/>
            </svg>
          </button>
        </div>
      </div>
    </section>
  )
}

export default Cases